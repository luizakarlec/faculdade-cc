    public void preencheTabela(){
        ProdutoDAO pDAO = new ProdutoDAO();
        
        List<Produto> listaProdutos = pDAO.getProdutosFiltro(txtNome.getText(),(Categoria) cmbCategoria.getSelectedItem()); 
        
        DefaultTableModel tabelaPessoas = (DefaultTableModel) tblProduto.getModel();
        tabelaPessoas.setNumRows(0);
        
        for(Produto p: listaProdutos){
            Object[] obj = new Object[]{
                p.getNome(),
                p.getPreco(),
                p.getQuantidade(),
                p.getCategoriaId().getNome()
            };
            tabelaPessoas.addRow(obj);
        }
    }

