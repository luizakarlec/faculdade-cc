    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        Veiculo v = new Veiculo();
        v.setMarca(txtMarca.getText());
        v.setModelo(txtModelo.getText());
        v.setAno(Integer.parseInt(txtAno.getText()));
        
        if(radPreto.isSelected()){
            cor = "Preto";
        } else if (radBranco.isSelected()){
            cor = "Branco";
        } else if (radPrata.isSelected()){
            cor = "Prata";
        } else if (radVermelho.isSelected()){
            cor = "Vermelho";
        } else if (radAzul.isSelected()){
            cor = "Azul";
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma cor", "Erro!", JOptionPane.OK_OPTION);
        }
        
        v.setPlaca(txtPlaca.getText());
        v.setCor(cor);
        
        VeiculoDAO vDAO = new VeiculoDAO();
        vDAO.inserir(v);
        limparFormulario();
        JOptionPane.showMessageDialog(null, "Veiculo inserido com sucesso!", "Sucesso!!", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_btnInserirActionPerformed

